######################## STRING ##################################

# FUNÇÃO TITLE #
name = "henrique lira"
print(name.title())

# FUNÇÃO LOWER #
name = "HENRIQUE LIRA"
print(name.lower())

# FUNÇÃO UPPER #
name = "henrique lira"
print(name.upper())

# CONCATENAÇÃO DE STRINGS #
name 		 = "henrique "
sobrenome 	 = "lira"
nomeCompleto = name + sobrenome
print(nomeCompleto.title())

# ACRESCENTANDO QUEBRA DE LINHA #
print("Linguagem : \n\t python\n \t C\n\tJavascript")

# REMOVENDO ESPAÇO EM BRANCO #

# FUNÇÃO rstrip #
linguagem = "python "
linguagem = linguagem.rstrip()
print(linguagem)

#FUNÇÃO lstrip #
linguagem = " python"
linguagem = linguagem.lstrip()
print(linguagem)

#FUNÇÃO STRIP #
linguagem = " python "
linguagem = linguagem.strip()
print(linguagem)




